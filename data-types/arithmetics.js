function arithmetics(input) {
  const numberOne = Number(input[0]);
  const numberTwo = Number(input[1]);
  console.log(numberOne + numberTwo);
  console.log(numberOne - numberTwo);
  console.log(numberOne * numberTwo);
  console.log(numberOne % numberTwo);
  console.log(numberOne ** numberTwo);
}
arithmetics(['3', '1']);
